USE CoogTechSolutions

set @StartDate = Convert(date, (DATEADD(year, -1, getdate()))) --One year ago today--

SELECT CUSTOMER.C_FNAME AS "First Name", CUSTOMER.C_LNAME AS "Last Name"
FROM Customer
JOIN CUSTOMER_STATUS
ON Customer.CUSTOMER_ID = CUSTOMER_STATUS.V_VIN
WHERE CUSTOMER_STATUS.ACTIVE_ID = 2 OR CUSTOMER_STATUS.ACTIVE_ID = 4
JOIN CUSTOMER_ORDER
ON Customer.CUSTOMER_ID = CUSTOMER_ORDER.CUSTOMER_ID
JOIN SERVICE_ORDER
ON CUSTOMER_ORDER.SERVICE_ORDER_ID = SERVICE_ORDER.SERVICE_ORDER_ID
WHERE SERVICE_ORDER.DATE_END < Convert(date, (DATEADD(year, -1, getdate())))
ORDER BY CUSTOMER.C_LNAME;
