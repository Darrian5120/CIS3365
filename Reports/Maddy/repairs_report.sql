USE CoogTechSolutions

DECLARE 
@serviceRepairs varchar(7),
@trailerRepairs varchar(15)

SELECT @serviceRepairs = 'REPAIRS'
SELECT @trailerRepairs = 'TRAILER REPAIRS'
SELECT 
Service.SERVICE_ID AS 'Service ID', 
Service.SERVICE_TYPE AS 'Service Type'

FROM SERVICE_LINE
JOIN SERVICE ON SERVICE_LINE.SERVICE_ID = SERVICE.SERVICE_ID
JOIN EMPLOYEE_SERVICE_LINE_ASSIGNMENT ON SERVICE_LINE.SERVICE_LINE_ID = EMPLOYEE_SERVICE_LINE_ASSIGNMENT.SERVICE_LINE_ID
JOIN SERVICE_ORDER ON SERVICE_LINE.SERVICE_ORDER_ID = SERVICE_ORDER.SERVICE_ORDER_ID
JOIN SERVICE_LINE_PART ON SERVICE_LINE.SERVICE_LINE_ID = SERVICE_LINE_PART.SERVICE_LINE_ID

WHERE Service.SERVICE_TYPE like @serviceRepairs
OR Service.SERVICE_TYPE like @trailerRepairs
